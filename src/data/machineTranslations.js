function mergeMachineContent(machine, translation) {
  if (!translation) {
    return machine
  }

  return {
    ...machine,
    ...translation,
  }
}

export function getLocalizedMachine(machine, language) {
  return mergeMachineContent(machine, machineTranslations[language]?.[machine.id])
}
